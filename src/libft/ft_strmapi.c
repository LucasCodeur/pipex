#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*dest;
	size_t			size;

	i = 0;
	size = ft_strlen(s);
	dest = ft_calloc(size + 1, sizeof(char));
	if (!dest)
		return (dest);
	while (s[i])
	{
		dest[i] = f(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
